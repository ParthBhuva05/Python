print("1---------------Pyramid Pattern---------------")
# Pyramid Pattern

size = 5

for i in range(1, size + 1):
    print(" " * (size - i) + "* " * i)



print("2---------------Inverted Pyramid Pattern---------------")
# Inverted Pyramid Pattern

size = 5

for i in range(size, 0, -1):
    print(" " * (size - i) + "* " * i)



