# Docstring in python 

# Python docstring are the string literals that appear right after the definition of a function , method , class or modules.

# We can access these doc string using the doc attribute.

# Python Doc attribute------> Whenever string literals are present just after the definition of a function, module, class or method, they are associated with the object as their doc attribute, wecan later use this attribute to retrieve this docstring.


def square(n):
    '''Takes in a number n, returns the square of n'''
    print(n**2)
square(5)
print(square.__doc__)


