# String slicing and operations on string in python



names = "parth,kevin" 
print(names[0:5])

# Length of a string 
print(len(names))

fruit = "mango"
print(fruit[0:4])
print(fruit[1:4])
print(fruit[:5])
# Negative slicing
print(fruit[0:len(fruit)-3])
print(fruit[-3:-1])

# Quick Quiz
nm = "Harry"
print(nm[-4:-2])



