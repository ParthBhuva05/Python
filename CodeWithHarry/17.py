# For loop in python  

name = 'abhishek'
for i in name :
    print(i)

colors = ["Red","Blue","Green","Yellow","Black"]
for x in colors:
    print(x)
    for j in x:
        print(j)


for k in range(20):
    print(k + 1)

for k in range(1, 20):
    print(k)

for k in range(1, 20, 2):
    print(k)
