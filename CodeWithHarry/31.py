# Set in python

set1 = {2, 4, 2, 6}
print(set1)
print(type(set1))
print("The Lenght of Set is: ", len(set1))

print("------------------------------")

print("Print The Set Using 'For Loop'.")
for x in set1:
    print(x)

print("------------------------------")

